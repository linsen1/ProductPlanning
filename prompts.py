# ── 辅助：格式化检索到的案例 ─────────────────────────────────────────────────
def format_retrieved_cases(docs: list[str]) -> str:
    if not docs:
        return "（暂未检索到相关历史案例，请根据产品信息自主生成，确保内容合理专业）"
    parts = []
    for i, doc in enumerate(docs, 1):
        parts.append(f"### 参考案例 {i}\n{doc}")
    return "\n\n".join(parts)


# ── 辅助：格式化硬命中的知识库块（结构/证书卡/合规）──────────────────────────
def format_kb_blocks(items: list[tuple]) -> str:
    """items: [(标题, 正文), ...]，硬命中内容直接整块注入"""
    if not items:
        return ""
    parts = []
    for title, body in items:
        parts.append(f"#### 〔{title}〕\n{body.strip()}")
    return "\n\n".join(parts)


# ── 辅助：格式化博查搜索结果 ──────────────────────────────────────────────────
def format_bocha_results(results: list[dict]) -> str:
    """将博查API返回的搜索结果格式化为提示词可用的文本（尽量榨干每条的可用文本）"""
    if not results:
        return "（未获取到竞品搜索数据，请改用内置竞品库并标注'待核实'进行分析）"
    parts = [f"（共 {len(results)} 条搜索结果，请逐条提炼机构名/价格/卖点/弱点）"]
    for i, r in enumerate(results, 1):
        name     = r.get("name", "") or r.get("title", "")
        url      = r.get("url", "")
        site     = r.get("siteName", "") or r.get("siteIcon", "")
        snippet  = (r.get("snippet", "") or "").strip()
        summary  = (r.get("summary", "") or "").strip()
        # 摘要与片段都给，summary 更长则放前面，避免信息丢失
        text = summary
        if snippet and snippet not in summary:
            text = (summary + "\n  片段：" + snippet) if summary else snippet
        parts.append(f"**结果{i}**：{name}（{site}）\n来源：{url}\n摘要：{text}")
    return "\n\n".join(parts)
