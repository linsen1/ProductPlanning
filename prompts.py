    category_keywords = []
    project_type_text = f"{project_type} {product_name}"
    if any(k in project_type_text for k in ["建筑", "一建", "二建", "造价", "监理", "消防"]):
        category_keywords.append("工程 建造师 造价 监理 消防 证书考试 长考")
    if any(k in project_type_text for k in ["医疗", "卫生", "营养", "健康", "养老", "心理", "康养"]):
        category_keywords.append("康养 医卫 健康管理 营养 心理 养老 证书")
    if any(k in project_type_text for k in ["公考", "国考", "省考", "事业", "教师", "法考", "考研", "党校", "研究生"]):
        category_keywords.append("考研 公考 招录 备考规划 岗位 选岗 年度映射")
